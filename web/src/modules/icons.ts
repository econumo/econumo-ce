export const availableIcons = _.uniq(icons).slice(0, 45);


export function iconsPagination(rows: number, cols: number): string[][] {
  const list = _.uniq(icons);
  const result = [];
  const size = rows * cols;
  for (let i = 0; i < list.length; i += size) {
    result.push(list.slice(i, i + size));
  }
  return result;
}

